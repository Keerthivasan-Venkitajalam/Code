// Relational operators, check the relation between two

// ==, >=, <=, >, <, !=

// output is 1 (if true) and 0 (if false)

#include<stdio.h>

int main()
{

    printf(" check 3=3; %d \n", 3==3);
    printf(" check 10=3; %d \n", 10==3);

    printf(" check 10>3; %d \n", 10>3);
    printf(" check 3>3; %d \n", 3>3);
    printf(" check 3>=3; %d \n", 3>=3);
    printf(" check 10<3; %d \n", 10<3);
    printf(" check 3<=3; %d \n", 3<=3);
    printf(" check 1<=3; %d \n", 1<=3);

    printf(" check !=, 10 !=3; %d \n", 10!=3);
    printf(" check 3!=3; %d \n", 3!=3);


    return 0;
}