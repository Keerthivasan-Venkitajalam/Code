public class CastExample {
    public static void main(String[] args){
        double a;
        int i;
        a=13;
        i=(int)a/2;
        System.out.println("The value of i is: "+i);

        char ch;
        ch=89;
        System.out.println("The value of ch is: "+ch);

        byte b;
        b=(byte)500;
        System.out.println("The value of b is: "+b);
    }
}
